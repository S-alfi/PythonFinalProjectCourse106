

# Topic 01: Arithmetic Operators

num1 = 4
num2 = 5
num3 = 70


# An example of addition result variable (sum)

addition = num1 + num2

# An example of subtraction result variable (difference)

subtraction = num3 - num2

# An example of multiplication result variable (product)

multiplication = num2 * num3

# An example of precise division result variable (precise quotient)

division_01 = num3 / num1

# An example of rounded division result variable (rounded quotient)

division_02 = num3 // num1

# The modulo operator (%) returns the remainder of a division problem between two integers.

modulo = num3 % num1

# An example of power / exponentiation by 2 result variable (square)

square = num1 ** 2

# An example of power / exponentiation by X result variable (power)

power = num1 ** 5

print()
print(f'1. An example of addition variable: {addition}')
print(f'2. An example of subtraction variable: {subtraction}')
print(f'3. An example of multiplication variable: {multiplication}')
print(f'4. An example of precise division variable: {division_01}')
print(f'5. An example of rounded division variable: {division_02}')
print(f'6. An example of modulo variable: {modulo}')
print(f'7. An example of square variable: {square}')
print(f'8. An example of power variable: {power}')

