

# Topic 10: Pytest - assert

num1 = 5
num2 = 4

summary = num1 + num2

# In Python, assert is used to test if a condition is true. If the condition is false, Python raises an error (AssertionError).

assert summary == 8, "The summary of num1 + num2 should be 8, the result doesn't match."


